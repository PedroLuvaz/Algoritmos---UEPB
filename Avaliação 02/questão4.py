#Faça um programa para somar os números pares positivos < 1000 e ao final escrever o resultado.
numero = 1000                        #exemplo = numero = 8
resultado = 0                        #resultado = 0 + 0
for n in range(0,numero+1,2):        #resultado = 2 + 0
     resultado += n                  #resultado = 4 + 2
                                     #resultado = 6 + 6 
print(resultado)                     #resultado = 8 + 12